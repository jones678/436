s designed and developed by a faculty member in Marshall University's Integrated Science and Technology Department. Originally, B.R.I.T.E. was built with a variety of technologies, including Oracle, mySQL, and PHP. Today, refinement of the software includes a 3-tiered web architecture built with PHP, Oracle, and jQuery.
